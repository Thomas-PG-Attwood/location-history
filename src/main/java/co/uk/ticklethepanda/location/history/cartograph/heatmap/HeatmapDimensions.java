package co.uk.ticklethepanda.location.history.cartograph.heatmap;

public class HeatmapDimensions {
    private final int width;
    private final int height;

    public HeatmapDimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
